// Helper function to get all items (including potion variants)
export function getAllItems(): string[] {
  return [...ALL_ITEMS, ...POTION_VARIANTS];
}

// Helper function to get items by category
export function getItemsByCategory(category: ItemCategory): string[] {
  if (category === 'all') {
    return getAllItems();
  }
  if (category === 'brewing') {
    // Include potion variants in the brewing category
    return [...MINECRAFT_ITEMS.brewing, ...POTION_VARIANTS];
  }
  return MINECRAFT_ITEMS[category] || [];
}

