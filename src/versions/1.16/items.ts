export function getCraftableItemsByCategory(category: ItemCategory): string[] {
  if (category === 'all') {
    return getCraftableItems();
  }
  return (MINECRAFT_ITEMS[category] || []).filter(item => !NON_CRAFTABLE_ITEMS.has(item));
}
